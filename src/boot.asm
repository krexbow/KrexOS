[org 0x7c00]
bits 16

KERNEL_OFFSET equ 0x1000    ; Адрес в ОЗУ, куда BIOS загрузит наше Си-ядро

start:
    cli                     ; Отключаем прерывания на время настройки сегментов
    xor ax, ax
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov bp, 0x7c00
    mov sp, bp
    sti                     ; Включаем обратно

    ; --- ЧИТАЕМ СИ-ЯДРО ИЗ СЕКТОРОВ HDD ---
    mov bx, KERNEL_OFFSET   ; Буфер в памяти (ES:BX = 0x0000:0x1000)
    mov ah, 0x02            ; Функция BIOS: Чтение секторов
    mov al, 50              ; Сколько секторов читать (берём с запасом под разросшееся ядро)
    mov ch, 0x00            ; Цилиндр 0
    mov dh, 0x00            ; Головка 0
    mov cl, 0x02            ; Начинаем строго со 2-го сектора (сразу после загрузочного)
    mov dl, 0x80            ; 0x80 = Первый жесткий диск (заменяем капризный флоппи 0x00)

    int 0x13
    jc .disk_error          ; Если BIOS выкинул флаг ошибки — падаем в бесконечный цикл

    ; --- ПЕРЕХОД В 32-БИТНЫЙ ЗАЩИЩЕННЫЙ РЕЖИМ ---
    cli
    lgdt [gdt_descriptor]   ; Загружаем таблицу дескрипторов сегментов
    mov eax, cr0
    or eax, 0x1             ; Включаем защищенный режим в CR0
    mov cr0, eax

    ; Длинный прыжок для очистки конвейера процессора и установки CS = 0x08
    jmp 0x08:init_pm

.disk_error:
    jmp $                   ; Зависаем тут, если диск не прочитался

; ==============================================================================
bits 32
init_pm:
    ; Настраиваем селекторы данных на GDT Data (индекс 0x10)
    mov ax, 0x10
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax
    mov ss, ax

    ; Выделяем безопасное место под стек ядра подальше от самого кода
    mov ebp, 0x90000
    mov esp, ebp

    ; Прыгаем выполнять код kernel_entry, который прилинкован к началу ядра
    call KERNEL_OFFSET

.halt:
    cli
    hlt
    jmp .halt

; ==============================================================================
align 4
gdt_start:
    dd 0x0, 0x0             ; Пустой дескриптор (обязателен)
gdt_code:
    dw 0xffff, 0x0000
    db 0x00, 10011010b, 11001111b, 0x00
gdt_data:
    dw 0xffff, 0x0000
    db 0x00, 10010010b, 11001111b, 0x00
gdt_end:

gdt_descriptor:
    dw gdt_end - gdt_start - 1
    dd gdt_start

; --- СТРОГАЯ РАЗМЕТКА ДИСКА (512 БАЙТ) ---
times 510 - ($ - $$) db 0
dw 0xaa55                   ; Магическая сигнатура загрузочного сектора

; СЮДА СВЕРХУ ЛОЖИТСЯ СКОМПИЛИРОВАННОЕ ЯДРО (ФИЗИЧЕСКИЙ ВТОРОЙ СЕКТОР ДИСКА)
incbin "kernel.bin"

; Добиваем финальный образ до размера ровно под геометрию, чтобы QEMU не ругался
times 1474560 - ($ - $$) db 0